
from datetime import datetime
import json

start_time = datetime(2022, 8, 8)
end_time = datetime(2022, 8, 9)

slice_unit = 100
x_axis = []
temp_time = start_time
cdf = []
original_list = []
for i in range(slice_unit):
    x_axis.append(temp_time)
    temp_time +=(end_time - start_time)/slice_unit


# load the file with time info
json_file = {}
with open("report.json","r") as report_file:
    json_file = json.load(report_file)

for i in json_file:
    keys_list = list(i.keys())
    timestamp = i[keys_list[1]]["timestamp"]
    datetime_timestamp = datetime.strptime(timestamp, "%Y-%m-%d %H:%M:%S.%f")
    original_list.append(datetime_timestamp)
        
for i in x_axis:
    cnt = 0
    for j in original_list:
        if(j < i):
            cnt = cnt +1
        else:
            cdf.append(cnt)
            break


print(cdf)


















































































































































































































































































































































































